//
// gen-manifest.js — generate the code-first file inventory (per-dir groups, line
// counts, byte sizes, stub flags) so the manifest can never silently drift from
// reality (the failure: a hand-authored manifest claiming "33 modules" over a
// real 89-file tree). Regenerates ONLY between the GEN markers; hand-authored
// prose outside the markers is preserved verbatim on every run.
//
//   node tools/gen-manifest.js [--config <path>]
//   node tools/gen-manifest.js --help
//
// Config (optional cfg.manifest): out (default FILE-MANIFEST.md),
// scanRoots (default from source.codeGlobs), stubMaxBytes (default 100).
// Idempotent: no timestamps → a clean re-run is byte-identical.

'use strict';
const fs = require('fs');
const path = require('path');

if (process.argv.includes('--help')) {
  console.log('gen-manifest — code-first inventory (line counts + stub flags) between GEN markers.\n' +
    'Usage: node tools/gen-manifest.js [--config <path>]');
  process.exit(0);
}

function loadConfig() {
  const argi = process.argv.indexOf('--config');
  let p = argi >= 0 ? process.argv[argi + 1] : null;
  if (!p) { let d = process.cwd(); while (d && d !== path.dirname(d)) { const c = path.join(d, 'stack.config.json'); if (fs.existsSync(c)) { p = c; break; } d = path.dirname(d); } }
  if (!p || !fs.existsSync(p)) { console.error('gen-manifest: no stack.config.json found.'); process.exit(2); }
  const cfg = JSON.parse(fs.readFileSync(p, 'utf8'));
  cfg.__repoRoot = (cfg.project && cfg.project.repoRoot) || path.dirname(p);
  return cfg;
}
const CFG = loadConfig();
const ROOT = CFG.__repoRoot;
const MAN = CFG.manifest || {};
const OUT = path.join(ROOT, MAN.out || 'FILE-MANIFEST.md');
const STUB_MAX = MAN.stubMaxBytes || 100;
const SCAN = (MAN.scanRoots || ((CFG.source && CFG.source.codeGlobs) || ['js/**/*.js']).map(g => {
  const ext = g.slice(g.lastIndexOf('.')); const root = g.split('/**')[0].split('/*')[0];
  return { root, ext };
}));

const GEN_START = '<!-- GEN:MANIFEST START — regenerated by tools/gen-manifest.js; do not edit between markers -->';
const GEN_END = '<!-- GEN:MANIFEST END -->';

function walk(dir, ext, out) {
  if (!fs.existsSync(dir)) return out;
  for (const e of fs.readdirSync(dir, { withFileTypes: true }).sort((a, b) => a.name.localeCompare(b.name))) {
    const p = path.join(dir, e.name);
    if (e.isDirectory()) walk(p, ext, out);
    else if (e.isFile() && p.endsWith(ext)) out.push(p);
  }
  return out;
}
function lineCount(p) { const b = fs.readFileSync(p, 'utf8'); if (!b.length) return 0; return b.split('\n').length - (b.endsWith('\n') ? 1 : 0); }
function esc(s) { return String(s).replace(/\|/g, '\\|'); }
function table(rows) { return rows.map(r => '| ' + r.join(' | ') + ' |').join('\n'); }

function inventory() {
  const groups = new Map(); const stubs = []; let total = 0, totalLines = 0;
  for (const s of SCAN) {
    const base = path.join(ROOT, s.root);
    for (const f of walk(base, s.ext, [])) {
      const relFromBase = path.relative(base, f);
      const dir = path.dirname(relFromBase) === '.' ? s.root + '/ (top-level)' : s.root + '/' + path.dirname(relFromBase);
      const size = fs.statSync(f).size, lines = lineCount(f), isStub = size <= STUB_MAX;
      if (isStub) stubs.push(path.relative(ROOT, f));
      if (!groups.has(dir)) groups.set(dir, []);
      groups.get(dir).push({ rel: path.relative(ROOT, f), name: path.basename(f), lines, size, isStub });
      total++; totalLines += lines;
    }
  }
  return { groups, stubs, total, totalLines };
}

function body() {
  const { groups, stubs, total, totalLines } = inventory();
  const out = [GEN_START, '',
    '## Code inventory (generated)', '',
    '**' + total + ' source files · ' + totalLines.toLocaleString() + ' lines · ' + stubs.length + ' stubs (≤ ' + STUB_MAX + ' bytes).**', ''];
  for (const dir of [...groups.keys()].sort()) {
    const files = groups.get(dir);
    out.push('### ' + dir + ' (' + files.length + ')'); out.push('');
    out.push('| file | lines | bytes | |'); out.push('|---|---:|---:|---|');
    out.push(table(files.map(f => [esc(f.rel), String(f.lines), String(f.size), f.isStub ? '`STUB`' : '']))); out.push('');
  }
  out.push(GEN_END);
  return out.join('\n');
}

function main() {
  const gen = body();
  let existing = '';
  try { existing = fs.readFileSync(OUT, 'utf8'); } catch (e) {}
  let next;
  if (existing.indexOf(GEN_START) >= 0 && existing.indexOf(GEN_END) >= 0) {
    next = existing.replace(new RegExp(GEN_START.replace(/[.*+?^${}()|[\]\\]/g, '\\$&') + '[\\s\\S]*?' + GEN_END.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')), gen);
  } else {
    const header = existing || ('# FILE-MANIFEST\n\n> Hand-authored prose lives outside the GEN markers and is preserved.\n\n');
    next = header.replace(/\n*$/, '\n\n') + gen + '\n';
  }
  fs.writeFileSync(OUT, next);
  const { total, totalLines } = inventory();
  console.log('FILE-MANIFEST written: ' + total + ' files, ' + totalLines + ' lines.');
}
main();
